void Powerup::draw(glm::mat4 VP)
{
    Matrices.model = glm::mat4(1.0f);
    glm::mat4 translate = glm::translate(this->position); // glTranslatef
    // glm::mat4 translate = glmq::translate (glm::vec3(0,0,0));    // glTranslatef
    glm::mat4 rotate = glm::rotate((float)(this->rotation * M_PI / 180.0f), glm::vec3(0, 0, 1));
    Matrices.model *= (translate * rotate);
    glm::mat4 MVP = VP * Matrices.model;
    glUniformMatrix4fv(Matrices.MatrixID, 1, GL_FALSE, &MVP[0][0]);
    draw3DObject(this->object);
}

void Powerup::set_position(float x, float y, float z)
{
    this->position = glm::vec3(x, y, z);
}

void Powerup::tick()
{
    this->rotation += 3;
    if (this->position.z <= 0)
    {
        this->speed.z = 0.01;
    }
    if (this->position.z > 1)
    {
        this->speed.z = -0.01;
    }
    this->position += this->speed;
}
bounding_box_t Powerup::bounding_box()
{
    bounding_box_t bbox = {this->position.x, this->position.y, this->position.z, 2, 2, 2};
    return bbox;
}